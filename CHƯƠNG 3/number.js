// trong chương 2 chúng ta đã tìm hiểu về cách chuyển từ chuổi sang number bằng các hàm 
console.log(Number(4.5)); // in ra kiểu số float
console.log(Number(4)); // in ra kiểu số int
console.log(Number(NaN)); // in ra NaN rỗng
console.log(Number(undefined)); // in ra NaN rỗng vì ko xác định đc dõ gia trị
console.log(Number(true)); // in ra giá trị = 1
console.log(Number(false)); // in ra giá trị = 0
console.log(Number('')); // in ra = 0 vid rỗng